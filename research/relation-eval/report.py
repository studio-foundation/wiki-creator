#!/usr/bin/env python3
"""Score every arm against the gold and write results.md.

recommendation.md is hand-written and inlined; everything else is generated.

Usage:
    python report.py
"""
import argparse
import glob
import json
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

import yaml  # noqa: E402

from score import gold_support, score  # noqa: E402

CAVEATS = """## Caveats

Read these before quoting any number.

1. **The gold is LLM-built, and `llm_schema` is an LLM.** Its score partly measures
   two LLM runs agreeing, biased upward by every convention they share. A different
   model (sonnet vs the gold's opus) and a production-shaped task (chunks, not whole
   chapters) blunt this; nothing removes it. Read that arm as an upper bound.
   **GLiREL vs co-occurrence survives the confound** — neither is an LLM, so neither
   is flattered by the gold's provenance. That is the comparison the ticket asked for.

2. **GLiREL's labels are guesses.** `research/ner-eval/sweep_labels.py` exists
   because wording moved the NER side's macro F1 from 0.840 to 0.866. There is no
   sweep here. A GLiREL loss means "not demonstrated", never "GLiREL cannot"; a
   GLiREL win needs no such caution. `other` is not asked for at all — our
   vocabulary defines it as "none of the above", and given a label GLiREL matched
   it to everything.

3. **GLiREL cannot say "symmetric".** It emits a directed head -> tail triple, and
   most gold relations are `symétrique`, so it is scored on an axis it was not
   built to express. That is a real limit for this pipeline, which needs direction
   — not a rigged comparison.

4. **The gold is one book, one annotator, one pass.** No second annotator, so no
   inter-annotator agreement, so the gold's own error rate is unknown.

5. **Both co-occurrence arms score zero on typing by construction.**
   `build_cooccurrence_graph` emits `relationship_type: None`; typing is a separate
   LLM stage. Read them on detection. Scoring them on type would score the
   classifier bolted after them.

6. **The oracle roster is an oracle.** Arms are handed a PERSON roster an LLM
   re-typed from the pipeline's own extractions. It is not a proposed fix and it
   is not achievable today — it exists so the benchmark can measure relation
   discovery rather than entity typing. `cooccurrence_shipped` runs on the roster
   production actually builds; the gap between it and `cooccurrence_fixed` mixes
   both effects, which is why the entity-layer table is reported separately.
"""


def _pct(v) -> str:
    return "—" if v is None else f"{v:.3f}"


def _with_n(cell: dict | None) -> str:
    """Accuracy is meaningless without the count it was taken over."""
    return "—" if cell is None else f"{cell['accuracy']:.3f} (n={cell['n']})"


def arm_rows(name: str, result: dict) -> str:
    rows = []
    for stratum in ("overall", "explicit", "implicit"):
        s = result[stratum]
        d = s["detection"]
        rows.append(" | ".join([
            f"| {name}", stratum, str(s["n_gold"]),
            _pct(d["precision"]), _pct(d["recall"]), _pct(d["f1"]),
            _pct(s["typing"]["accuracy_end_to_end"]),
            _with_n(s["typing_conditional"]),
            _with_n(s["direction"]) + " |",
        ]))
    return "\n".join(rows)


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--gold", default="gold.yaml")
    ap.add_argument("--out", default="results.md")
    args = ap.parse_args()

    with open(args.gold, encoding="utf-8") as f:
        gold = yaml.safe_load(f)["pairs"]

    results = {}
    for path in sorted(glob.glob("predictions.*.json")):
        arm = os.path.basename(path).split(".")[1]
        with open(path, encoding="utf-8") as f:
            results[arm] = score(gold, json.load(f))

    n_implicit = sum(1 for p in gold if p["implicit"])
    lines = [
        "# Relation-discovery bake-off — results (STU-467)",
        "",
        "Generated by `report.py`. Do not edit.",
        "",
        "## Gold",
        "",
        f"- {len(gold)} book-level pairs on Eragon, {n_implicit} implicit "
        f"({n_implicit / len(gold):.0%}) — never within one sentence of each other anywhere.",
        f"- per primary type: `{gold_support(gold)}`",
        "",
        "## Arms",
        "",
        "| arm | stratum | gold n | det P | det R | det F1 | type acc (e2e) | type acc (cond.) | direction |",
        "|---|---|---|---|---|---|---|---|---|",
    ]
    for arm, result in results.items():
        lines.append(arm_rows(arm, result))
    lines += ["", f"Pairs predicted: `{ {a: r['n_predicted'] for a, r in results.items()} }`", ""]

    recommendation = "recommendation.md"
    if os.path.exists(recommendation):
        with open(recommendation, encoding="utf-8") as f:
            lines += [f.read(), ""]

    lines.append(CAVEATS)

    with open(args.out, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"{len(results)} arms scored against {len(gold)} gold pairs -> {args.out}")


if __name__ == "__main__":
    main()
